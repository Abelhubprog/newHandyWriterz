import React from 'react';
import { cva, type VariantProps } from 'class-variance-authority';
import { cn } from '@/utils/cn';

const cardVariants = cva(
  'rounded-lg bg-white border transition-shadow',
  {
    variants: {
      variant: {
        default: 'border-gray-200 shadow-sm',
        elevated: 'border-gray-200 shadow-md',
        outline: 'border-gray-300 shadow-none',
      },
      hover: {
        true: 'hover:shadow-md hover:border-gray-300',
      },
      padded: {
        true: 'p-6',
        false: '',
      },
    },
    defaultVariants: {
      variant: 'default',
      hover: false,
      padded: true,
    },
  }
);

export interface CardProps
  extends React.HTMLAttributes<HTMLDivElement>,
    VariantProps<typeof cardVariants> {
  as?: React.ElementType;
}

export const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant, hover, padded, children, as: Component = 'div', ...props }, ref) => {
    return (
      <Component
        ref={ref}
        className={cn(cardVariants({ variant, hover, padded, className }))}
        {...props}
      >
        {children}
      </Component>
    );
  }
);

Card.displayName = 'Card';

// Sub-components for consistent card sections
